
/*
Hàm REPLICATE() dùng để lặp lại n lần một chuỗi kí tự cho trước.
Cú pháp:
REPLICATE(string, n)
Trong đó cả hai tham số là bắt buộc.
string là chuỗi kí tự hoặc biến, biểu thức, tên cột chứa giá trị chuỗi kí tự cần nhân bản.
n là số nguyên dương chỉ định số lần cần nhân bản, lặp lại của tham số thứ nhất.
*/
